void memtest(struct Chip8 * c){
int i = 4096;
for(i=4096;i>0;i--)
	c->mem[i] = i;
}

int resetchip(struct Chip8 * c){
	memtest(c);
	int i=0;
	for (i=0;i<=80;i++)
	{	c->mem[80+i] = font[i];	}
	c->pc = 0x200;
	return c->pc;
}
int main (){
	struct Chip8 chip;
	int i;
	i = resetchip(&chip);
	if (i==0x200) 
		printf("reset OK");
	return 0;
}

